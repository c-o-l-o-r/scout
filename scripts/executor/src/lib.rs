#[macro_use]
extern crate ssz_derive;

use ewasm_api::*;
use sha3::{Digest, Keccak256};
use ssz::{Decode, Encode};

mod host {
    extern "C" {
        pub fn print(obj: *const u32, len: u32);
    }
}

#[derive(Debug, PartialEq, Ssz, Default)]
struct Transaction {
    pub target: u64,
    pub data: Vec<u8>,
}

#[derive(Clone, Debug, PartialEq, Ssz, Default)]
struct Storage {
    pub code: Vec<u8>,
    pub storage: Vec<u8>,
}

#[derive(Clone, Debug, PartialEq, Ssz, Default)]
struct State {
    pub storage: Vec<Storage>,
}

#[derive(Debug, PartialEq, Ssz, Default)]
struct InputBlock {
    pub transactions: Vec<Transaction>,
    pub state: State,
}

trait StateRoot {
    fn state_root(&self) -> types::Bytes32;
}

impl StateRoot for State {
    fn state_root(&self) -> types::Bytes32 {
        let serialised = self.encode();
        let hash = Keccak256::digest(&serialised[..]);
        let mut ret = types::Bytes32::default();
        ret.bytes.copy_from_slice(&hash[..]);
        ret
    }
}

#[derive(Debug, PartialEq, Ssz, Default)]
struct Context {
    pub state: State,
}

fn process_block(pre_state: types::Bytes32, mut block_data: &[u8]) -> types::Bytes32 {
    let mut block = InputBlock::decode(&mut block_data).expect("valid input");

    assert!(block.state.state_root().bytes == pre_state.bytes);

    let mut ctx = Context {
        state: block.state.clone(),
    };

    for tx in block.transactions {
        if tx.target == 0 {
            ctx.state.storage.push(Storage {
                code: tx.data,
                storage: vec![],
            });
        } else {
            eth2::exec_code(
                &ctx.state.storage[tx.target as usize].code,
                &tx.data,
                &ctx.encode(),
            );

            let mut return_data: &[u8] = &eth2::acquire_return_data()[..];
            ctx = Context::decode(&mut return_data).expect("valid context");

            unsafe {
                host::print(
                    ctx.state.storage[tx.target as usize].storage.as_ptr() as *const u32,
                    ctx.state.storage[tx.target as usize].storage.len() as u32,
                );
            }
        }
    }

    ctx.state.state_root()
}

#[cfg(not(test))]
#[no_mangle]
pub extern "C" fn main() {
    let pre_state = eth2::load_pre_state_root();
    let block_data = eth2::acquire_block_data();
    let post_state = process_block(pre_state, &block_data);
    eth2::save_post_state_root(post_state)
}

